// Variables
// Variables allow users to point to and refer to values in the JavaScript 
// data-space. All values that are initialized or simply exist in JavaScript 
// are accessed through variables. 
// Letâ€™s call them pathways to values. See how we can declare a variable


var variable; // declare variable named "variable"
console.log(variable);// print variable



// Assignment of variables
// To use value through a variable, we need to assign the variable to the data using the = operator.


var variable_1; // variable declaration
variable_1 = 25; // variable assigment
console.log("variable_1",variable_1); // variable printing

var variable_2 = 30; // variable declaration and assigment
console.log("variable_2",variable_2); // variable printing

// how values are stored in variable_1 and variable_2. 
// Using the = operator, we assign the values 25 and 30 
// respectively to the two variables. Observe that the 
// assignment operator can be used at the point of declaration or 
// after it.

variable_1 = 10; // declaration and assigment without var
console.log("variable_1",variable_1);


